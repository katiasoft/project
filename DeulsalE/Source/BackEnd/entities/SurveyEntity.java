package com.iforteam.deulsal_i.entities;

public class SurveyEntity {
    private int space;
    private int kid;
    private int isDog;
    private int type;
    private int leisure;
    private int fireStand;
    private int mart;


    public int getSpace() {
        return space;
    }

    public SurveyEntity setSpace(int space) {
        this.space = space;
        return this;
    }

    public int getKid() {
        return kid;
    }

    public SurveyEntity setKid(int kid) {
        this.kid = kid;
        return this;
    }

    public int getIsDog() {
        return isDog;
    }

    public SurveyEntity setIsDog(int isDog) {
        this.isDog = isDog;
        return this;
    }

    public int getType() {
        return type;
    }

    public SurveyEntity setType(int type) {
        this.type = type;
        return this;
    }

    public int getLeisure() {
        return leisure;
    }

    public SurveyEntity setLeisure(int leisure) {
        this.leisure = leisure;
        return this;
    }

    public int getFireStand() {
        return fireStand;
    }

    public SurveyEntity setFireStand(int fireStand) {
        this.fireStand = fireStand;
        return this;
    }

    public int getMart() {
        return mart;
    }

    public SurveyEntity setMart(int mart) {
        this.mart = mart;
        return this;
    }
}
